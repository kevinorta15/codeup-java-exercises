public class StringExercise
{
    public static void main(String[] args) {
       String doNotNeed = "We don't need no education";
        System.out.println(doNotNeed.replace("no education", "no thought control"));

        String checkitOut = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkitOut);

        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);

        String backslashMania = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backslashMania);
    }
}
